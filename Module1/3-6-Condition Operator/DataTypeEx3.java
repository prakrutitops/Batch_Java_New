
public class DataTypeEx3 
{
	
	public static void main(String[] args) 
	{
			int a= 3;
			int b=2;
			
			
			
			a+=b;//a=a+b
			System.out.println(a);//5
			
			a-=b;//a=a-b=5-2=3
			System.out.println(a);//3
			
			a*=b;
			System.out.println(a);
			
		
	}
	
	//a-=b//a=a-b
	
}
