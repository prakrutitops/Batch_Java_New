package com.a66;

public class SwitchEx 
{
	public static void main(String[] args) 
	{
		
		int num = 1;
		
		switch(num)
		{
			case 1:System.out.println("Your Selected Language is Gujarati");
			break;
			
			case 2:System.out.println("Your Selected Language is Hindi");
			break;
			
			case 3:System.out.println("Your Selected Language is English");	
			break;
		}
		
	}
}
